package kranji.codegen.depth1;

import kranji.json.dto.BlockRefJson;
import kranji.json.dto.ComposedBlockJson;
import kranji.json.dto.ComposedZiJson;
import kranji.json.dto.EtymologyJson;
import kranji.json.dto.PinyinJson;
import kranji.json.dto.SingularPartJson;
import kranji.pinyin.PinyinSyllable;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.Map;
import java.util.Set;
import java.util.TreeMap;
import java.util.TreeSet;

/**
 * Stateless source-text builder for the per-(initial, final, tone)
 * depth-1 classes (e.g. {@code k/Ong1.java}).
 *
 * <p>Pure function of:
 * <ul>
 *   <li>the package name + class name (deterministic from the routing key),</li>
 *   <li>the ordered list of {@code (fieldName, ComposedZiJson)} members,</li>
 *   <li>the global {@link SingularZiIndex} (glyph → Java reference).</li>
 * </ul>
 * Re-emitted from scratch on every run; identical input must produce
 * byte-identical output.</p>
 */
public final class Depth1JavaEmitter {

    public static final String AUTO_BANNER =
            "// AUTO-GENERATED by Depth1GenerateMain - do not edit by hand.";

    private Depth1JavaEmitter() {}

    /** Member tuple used by {@link #emitClass}. */
    public record Member(String fieldName, ComposedZiJson data) {}

    // ── Per-(final, tone) class ───────────────────────────────────────

    /**
     * Emit one {@code <Final><Tone>.java} file body.
     *
     * @param packageName e.g. {@code "kranji.common.depth1.k"}
     * @param className   e.g. {@code "Ong1"}
     * @param members     fields to emit, in any order — they are
     *                    sorted alphabetically by {@code fieldName}
     *                    so the {@code ALL} list is deterministic
     * @param index       glyph → Java reference table
     */
    public static String emitClass(String packageName,
                                   String className,
                                   List<Member> members,
                                   SingularZiIndex index) {
        // Stable order: alphabetical by field name.
        List<Member> sorted = new ArrayList<>(members);
        sorted.sort((a, b) -> a.fieldName().compareTo(b.fieldName()));

        // Collect refs so we can compute the import set.
        ImportCollector imports = new ImportCollector();
        imports.addCore("kranji.zi.ComposedZi");
        imports.addCore("kranji.zi.ZiChar");
        imports.addCore("kranji.zi.CompositionLayout.*");
        imports.addCore("kranji.zi.ComposedPart");
        imports.addCore("kranji.zi.SingularPart");
        imports.addCore("kranji.classification.EtymologicalCategory.*");
        imports.addCore("kranji.pinyin.Body");
        imports.addCore("kranji.pinyin.Final");
        imports.addCore("kranji.pinyin.Head");
        imports.addCore("kranji.pinyin.Initial");
        imports.addCore("kranji.pinyin.PinyinSyllable");
        imports.addCore("kranji.pinyin.Tail");
        imports.addCore("kranji.pinyin.Tone");
        imports.addCore("java.util.List");

        // First pass: build the body, recording referenced singulars so
        // their imports get pulled in.
        StringBuilder body = new StringBuilder();
        for (Member m : sorted) {
            body.append(emitField(m, index, imports)).append('\n');
        }

        // Final source.
        StringBuilder sb = new StringBuilder();
        sb.append(AUTO_BANNER).append('\n');
        sb.append("package ").append(packageName).append(";\n\n");
        for (String i : imports.sorted()) {
            sb.append("import ").append(i).append(";\n");
        }
        sb.append('\n');
        sb.append("public final class ").append(className).append(" {\n\n");
        sb.append("    private ").append(className).append("() {}\n\n");
        sb.append(body);
        // ALL list
        sb.append("    public static final List<ComposedZi> ALL = List.of(\n");
        for (int i = 0; i < sorted.size(); i++) {
            sb.append("            ").append(sorted.get(i).fieldName());
            sb.append(i == sorted.size() - 1 ? ");\n" : ",\n");
        }
        sb.append("}\n");
        return sb.toString();
    }

    // ── Per-initial aggregator (Depth1B, Depth1Zero, ...) ─────────────

    /** Emit a {@code Depth1<InitialPascal>.java} aggregator file body. */
    public static String emitInitialAggregator(String packageName,
                                               String aggregatorClassName,
                                               String subPackage,
                                               List<String> classNames) {
        // Deterministic order: alphabetical by class name.
        List<String> sorted = new ArrayList<>(classNames);
        Collections.sort(sorted);

        StringBuilder sb = new StringBuilder();
        sb.append(AUTO_BANNER).append('\n');
        sb.append("package ").append(packageName).append(";\n\n");
        sb.append("import kranji.zi.ComposedZi;\n\n");
        for (String cn : sorted) {
            sb.append("import ").append(packageName).append('.').append(subPackage)
                    .append('.').append(cn).append(";\n");
        }
        sb.append('\n');
        sb.append("import java.util.ArrayList;\n");
        sb.append("import java.util.Collections;\n");
        sb.append("import java.util.List;\n\n");
        sb.append("public final class ").append(aggregatorClassName).append(" {\n\n");
        sb.append("    private ").append(aggregatorClassName).append("() {}\n\n");
        sb.append("    public static final List<ComposedZi> ALL;\n");
        sb.append("    static {\n");
        sb.append("        var list = new ArrayList<ComposedZi>();\n");
        for (String cn : sorted) {
            sb.append("        list.addAll(").append(cn).append(".ALL);\n");
        }
        sb.append("        ALL = Collections.unmodifiableList(list);\n");
        sb.append("    }\n");
        sb.append("}\n");
        return sb.toString();
    }

    // ── Top-level Depth1 aggregator ───────────────────────────────────

    /** Emit the rewritten {@code Depth1.java}. */
    public static String emitTopLevelAggregator(String packageName,
                                                List<String> initialAggregatorClassNames) {
        List<String> sorted = new ArrayList<>(initialAggregatorClassNames);
        Collections.sort(sorted);

        StringBuilder sb = new StringBuilder();
        sb.append(AUTO_BANNER).append('\n');
        sb.append("package ").append(packageName).append(";\n\n");
        sb.append("import kranji.zi.ComposedZi;\n\n");
        sb.append("import java.util.ArrayList;\n");
        sb.append("import java.util.Collections;\n");
        sb.append("import java.util.List;\n\n");
        sb.append("/**\n");
        sb.append(" * Depth-1 composed characters: every leaf component is a singular block (depth 0).\n");
        sb.append(" *\n");
        sb.append(" * <p>Records are partitioned by pinyin (initial, final, tone) — see the\n");
        sb.append(" * {@code <initial>/<Final><Tone>.java} sub-package files. This class\n");
        sb.append(" * concatenates every per-initial aggregator into a single immutable list.</p>\n");
        sb.append(" */\n");
        sb.append("public final class Depth1 {\n\n");
        sb.append("    private Depth1() {}\n\n");
        sb.append("    /** All depth-1 composed characters. */\n");
        sb.append("    public static final List<ComposedZi> ALL;\n\n");
        sb.append("    static {\n");
        sb.append("        var list = new ArrayList<ComposedZi>();\n");
        for (String cn : sorted) {
            sb.append("        list.addAll(").append(cn).append(".ALL);\n");
        }
        sb.append("        ALL = Collections.unmodifiableList(list);\n");
        sb.append("    }\n");
        sb.append("}\n");
        return sb.toString();
    }

    // ── Field emission ────────────────────────────────────────────────

    private static String emitField(Member m, SingularZiIndex index, ImportCollector imports) {
        ComposedZiJson z = m.data();
        StringBuilder sb = new StringBuilder();
        sb.append("    public static final ComposedZi ").append(m.fieldName())
                .append(" = new ComposedZi(\n");
        // ziChar — always Unicode form for source-generation simplicity.
        sb.append("            new ZiChar.Unicode(").append(quoteUnicode(z.glyph())).append("),\n");
        // pinyin — typed.
        sb.append("            ").append(emitPinyin(z.pinyin())).append(",\n");
        // strokes / radicalNo
        sb.append("            ").append(z.strokes() == null ? 0 : z.strokes()).append(", ");
        sb.append(z.radicalNo() == null ? 0 : z.radicalNo()).append(", ");
        sb.append(quoteJava(z.meaning() == null ? "" : z.meaning())).append(",\n");
        // composition
        sb.append("            ").append(emitBlock(z.composition(), index, imports)).append(",\n");
        // etymology
        sb.append("            ").append(emitEtymology(z.etymology(), index, imports));
        sb.append(");\n");
        return sb.toString();
    }

    // ── Composition ───────────────────────────────────────────────────

    static String emitBlock(ComposedBlockJson block,
                            SingularZiIndex index,
                            ImportCollector imports) {
        String style = block.style();
        Map<String, BlockRefJson> slots = block.slots();
        return switch (style) {
            case "left_right"        -> "new LeftRight("
                    + emitRef(slots.get("left"), index, imports) + ", "
                    + emitRef(slots.get("right"), index, imports) + ")";
            case "top_bottom"        -> "new TopBottom("
                    + emitRef(slots.get("top"), index, imports) + ", "
                    + emitRef(slots.get("bottom"), index, imports) + ")";
            case "left_middle_right" -> "new LeftMiddleRight("
                    + emitRef(slots.get("left"), index, imports) + ", "
                    + emitRef(slots.get("middle"), index, imports) + ", "
                    + emitRef(slots.get("right"), index, imports) + ")";
            case "top_middle_bottom" -> "new TopMiddleBottom("
                    + emitRef(slots.get("top"), index, imports) + ", "
                    + emitRef(slots.get("middle"), index, imports) + ", "
                    + emitRef(slots.get("bottom"), index, imports) + ")";
            case "full_enclosure"    -> "new FullEnclosure("
                    + emitRef(slots.get("outer"), index, imports) + ", "
                    + emitRef(slots.get("inner"), index, imports) + ")";
            case "semi_enclosure_upper_left"  -> emitSemi("SemiEnclosureUpperLeft",  slots, index, imports);
            case "semi_enclosure_upper_right" -> emitSemi("SemiEnclosureUpperRight", slots, index, imports);
            case "semi_enclosure_bottom_left" -> emitSemi("SemiEnclosureBottomLeft", slots, index, imports);
            case "semi_enclosure_top_three"   -> emitSemi("SemiEnclosureTopThree",   slots, index, imports);
            case "semi_enclosure_bottom_three"-> emitSemi("SemiEnclosureBottomThree",slots, index, imports);
            case "semi_enclosure_left_three"  -> emitSemi("SemiEnclosureLeftThree",  slots, index, imports);
            default -> throw new IllegalArgumentException("Unknown composition style: " + style);
        };
    }

    private static String emitSemi(String variantName,
                                   Map<String, BlockRefJson> slots,
                                   SingularZiIndex index,
                                   ImportCollector imports) {
        return "new " + variantName + "("
                + emitRef(slots.get("wrapper"), index, imports) + ", "
                + emitRef(slots.get("content"), index, imports) + ")";
    }

    private static String emitRef(BlockRefJson ref,
                                  SingularZiIndex index,
                                  ImportCollector imports) {
        if (ref == null) {
            throw new IllegalArgumentException("Missing slot in composition");
        }
        return switch (ref) {
            case BlockRefJson.GlyphRef g -> {
                SingularZiIndex.Ref r = index.require(g.glyph());
                imports.addRef(r);
                yield r.expression();
            }
            case BlockRefJson.InlineComposed ic -> "new ComposedPart(" + emitBlock(ic.block(), index, imports) + ")";
            case BlockRefJson.InlineSingular is -> emitInlineSingular(is.part());
        };
    }

    private static String emitInlineSingular(SingularPartJson p) {
        // Uncommon at depth 1; inline construction with positional args
        // matching the SingularPart record's primary constructor.
        // SingularPart record signature is intentionally not coupled here
        // because depth1 doesn't actually ship any inline singulars today;
        // emit a compile-time error placeholder so missing coverage is loud.
        throw new UnsupportedOperationException(
                "InlineSingular emission for glyph \"" + p.glyph()
                        + "\" is not implemented — depth1 catalog should "
                        + "always reference singulars by glyph (GlyphRef).");
    }

    // ── Etymology ─────────────────────────────────────────────────────

    static String emitEtymology(EtymologyJson e,
                                SingularZiIndex index,
                                ImportCollector imports) {
        if (e == null) return "new Pictograph()";
        return switch (e) {
            case EtymologyJson.Pictograph p -> "new Pictograph()";
            case EtymologyJson.SimpleIndicative s ->
                    "new SimpleIndicative(" + quoteJava(s.indicatorDescription()) + ")";
            case EtymologyJson.CompoundIndicative c ->
                    "new CompoundIndicative(" + quoteJava(c.meaningHint()) + ")";
            case EtymologyJson.DerivativeCognate d ->
                    "new DerivativeCognate(" + quoteJava(d.cognateGlyph()) + ")";
            case EtymologyJson.PhoneticLoan l ->
                    "new PhoneticLoan(" + quoteJava(l.originalMeaning()) + ")";
            case EtymologyJson.PhonoSemantic ps -> "new PhonoSemantic("
                    + emitEtymRef(ps.semanticPart(), index, imports) + ", "
                    + emitEtymRef(ps.phoneticPart(), index, imports) + ")";
        };
    }

    private static String emitEtymRef(BlockRefJson ref,
                                      SingularZiIndex index,
                                      ImportCollector imports) {
        return emitRef(ref, index, imports);
    }

    // ── Pinyin ────────────────────────────────────────────────────────

    /**
     * Initials whose JSON {@code final == ""} corresponds to the
     * syllabic-consonant reading written orthographically as
     * {@code "i"} (zhi/chi/shi/ri/zi/ci/si). The bridge's pinyinFrom
     * passes the empty final straight through to PinyinSyllable.parse,
     * which doesn't accept "zh3"/"r3"/etc.; we substitute the canonical
     * "i" here so parsing succeeds and produces the expected
     * {@code (OPEN, NULL, NONE)} decomposition.
     */
    private static final Set<String> SYLLABIC_INITIALS_PINYIN = Set.of(
            "zh", "ch", "sh", "r", "z", "c", "s");

    static String emitPinyin(PinyinJson py) {
        if (py == null) return "null";
        String initial = py.initial() == null ? "" : py.initial();
        String fin     = py.fin()     == null ? "" : py.fin();
        int    tone    = py.tone()    == null ? 5  : py.tone();
        if (fin.isEmpty() && SYLLABIC_INITIALS_PINYIN.contains(initial)) {
            fin = "i";
        }
        // Parse to recover the typed (Head, Body, Tail) decomposition;
        // deterministic round-trip.
        PinyinSyllable typed = PinyinSyllable.parse(initial + fin + tone);
        if (typed == null) return "null";
        return "new PinyinSyllable("
                + "Initial." + typed.initial().name()
                + ", new Final("
                + "Head." + typed.fin().head().name()
                + ", Body." + typed.fin().body().name()
                + ", Tail." + typed.fin().tail().name() + "), "
                + "Tone." + typed.tone().name() + ")";
    }

    // ── String quoting (mirror of singulars/JavaEmitter) ──────────────

    static String quoteUnicode(String s) {
        StringBuilder b = new StringBuilder("\"");
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c == '"')       b.append("\\\"");
            else if (c == '\\') b.append("\\\\");
            else if (c == '\n') b.append("\\n");
            else if (c == '\r') b.append("\\r");
            else if (c == '\t') b.append("\\t");
            else if (c >= 0x20 && c < 0x7F) b.append(c);
            else b.append(String.format("\\u%04X", (int) c));
        }
        b.append('"');
        return b.toString();
    }

    static String quoteJava(String s) {
        StringBuilder b = new StringBuilder("\"");
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c == '"')       b.append("\\\"");
            else if (c == '\\') b.append("\\\\");
            else if (c == '\n') b.append("\\n");
            else if (c == '\r') b.append("\\r");
            else if (c == '\t') b.append("\\t");
            else                b.append(c);
        }
        b.append('"');
        return b.toString();
    }

    // ── Import collector ──────────────────────────────────────────────

    /**
     * Collects import paths and emits them in deterministic alphabetical
     * order. {@code java.*} imports are kept separate per project style.
     */
    static final class ImportCollector {
        private final Set<String> core   = new TreeSet<>();
        private final Set<String> singulars = new TreeSet<>();

        void addCore(String fqn) { core.add(fqn); }

        void addRef(SingularZiIndex.Ref r) {
            singulars.add(r.importFqn());
        }

        /** Returns merged sorted import list: core first, singulars second. */
        List<String> sorted() {
            List<String> out = new ArrayList<>();
            // Non-java first, java.* last per Kranji convention.
            Set<String> nonJava = new TreeSet<>();
            Set<String> java = new TreeSet<>();
            for (String s : core)      (s.startsWith("java.") ? java : nonJava).add(s);
            for (String s : singulars) (s.startsWith("java.") ? java : nonJava).add(s);
            out.addAll(nonJava);
            out.addAll(java);
            return out;
        }

        /** Visible for tests. */
        Map<String, Set<String>> snapshot() {
            Map<String, Set<String>> snap = new TreeMap<>();
            snap.put("core", new TreeSet<>(core));
            snap.put("singulars", new TreeSet<>(singulars));
            return snap;
        }
    }
}
