package kranji.codegen.depth2;

import kranji.codegen.depth1.SingularZiIndex;
import kranji.json.dto.BlockRefJson;
import kranji.json.dto.ComposedBlockJson;
import kranji.json.dto.ComposedZiJson;
import kranji.json.dto.EtymologyJson;
import kranji.json.dto.PinyinJson;
import kranji.json.dto.SingularPartJson;
import kranji.pinyin.PinyinSyllable;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import java.util.Map;
import java.util.Set;
import java.util.TreeMap;
import java.util.TreeSet;

/**
 * Source-text builder for depth-2 codegen. Mirrors
 * {@code Depth1JavaEmitter} but resolves {@link BlockRefJson.GlyphRef}
 * slots via two indexes in priority order:
 *
 * <ol>
 *   <li>{@link ComposedZiIndex} for depth-1 ComposedZi glyphs — emitted
 *       as {@code <Depth1Class>.<FIELD>.structure()} so the slot's
 *       {@link kranji.zi.BlockStructure} type is satisfied.</li>
 *   <li>{@link SingularZiIndex} for singular glyphs (the {@code 辶},
 *       {@code 扌}, etc. radicals and standalone singulars) — emitted
 *       as {@code <SingularClass>.<CONSTANT>}.</li>
 * </ol>
 *
 * <p>Output format and import rules match the depth-1 emitter exactly,
 * so the auto-generated source retains a uniform look across depth
 * buckets.</p>
 */
public final class Depth2JavaEmitter {

    /** Default banner (depth-2) — retained for source-compat with earlier callers. */
    public static final String AUTO_BANNER = bannerFor(2);

    /** Banner text for a specific depth bucket. */
    public static String bannerFor(int depth) {
        return "// AUTO-GENERATED by Depth" + depth
                + "GenerateMain - do not edit by hand.";
    }

    private Depth2JavaEmitter() {}

    /** Member tuple used by {@link #emitClass}. */
    public record Member(String fieldName, ComposedZiJson data) {}

    // ── Per-(final, tone) class ───────────────────────────────────────

    /**
     * Emit one {@code <Final><Tone>.java} file body.
     *
     * @param packageName e.g. {@code "kranji.common.depth2.f"}
     * @param className   e.g. {@code "En1"}
     * @param members     fields to emit, in any order — sorted
     *                    alphabetically by {@code fieldName} for
     *                    deterministic {@code ALL} output
     * @param singulars   glyph → singular Java reference
     * @param composed    glyph → depth-1 Java reference
     */
    public static String emitClass(String packageName,
                                   String className,
                                   List<Member> members,
                                   SingularZiIndex singulars,
                                   ComposedZiIndex composed) {
        return emitClass(2, packageName, className, members, singulars, composed);
    }

    /** Depth-parameterized variant — used by depth-3+ generators. */
    public static String emitClass(int depth,
                                   String packageName,
                                   String className,
                                   List<Member> members,
                                   SingularZiIndex singulars,
                                   ComposedZiIndex composed) {
        List<Member> sorted = new ArrayList<>(members);
        sorted.sort((a, b) -> a.fieldName().compareTo(b.fieldName()));

        ImportCollector imports = new ImportCollector();
        imports.addCore("kranji.zi.ComposedZi");
        imports.addCore("kranji.zi.ZiChar");
        imports.addCore("kranji.zi.CompositionLayout.*");
        imports.addCore("kranji.zi.ComposedPart");
        imports.addCore("kranji.zi.SingularPart");
        imports.addCore("kranji.classification.EtymologicalCategory.*");
        imports.addCore("kranji.pinyin.Body");
        imports.addCore("kranji.pinyin.Final");
        imports.addCore("kranji.pinyin.Head");
        imports.addCore("kranji.pinyin.Initial");
        imports.addCore("kranji.pinyin.PinyinSyllable");
        imports.addCore("kranji.pinyin.Tail");
        imports.addCore("kranji.pinyin.Tone");
        imports.addCore("java.util.List");

        StringBuilder body = new StringBuilder();
        for (Member m : sorted) {
            body.append(emitField(m, singulars, composed, imports)).append('\n');
        }

        StringBuilder sb = new StringBuilder();
        sb.append(bannerFor(depth)).append('\n');
        sb.append("package ").append(packageName).append(";\n\n");
        for (String i : imports.sorted()) {
            sb.append("import ").append(i).append(";\n");
        }
        sb.append('\n');
        sb.append("public final class ").append(className).append(" {\n\n");
        sb.append("    private ").append(className).append("() {}\n\n");
        sb.append(body);
        sb.append("    public static final List<ComposedZi> ALL = List.of(\n");
        for (int i = 0; i < sorted.size(); i++) {
            sb.append("            ").append(sorted.get(i).fieldName());
            sb.append(i == sorted.size() - 1 ? ");\n" : ",\n");
        }
        sb.append("}\n");
        return sb.toString();
    }

    // ── Per-initial aggregator (Depth2B, Depth2Zero, ...) ─────────────

    /** Emit a {@code Depth2<InitialPascal>.java} aggregator file body. */
    public static String emitInitialAggregator(String packageName,
                                               String aggregatorClassName,
                                               String subPackage,
                                               List<String> classNames) {
        return emitInitialAggregator(2, packageName, aggregatorClassName, subPackage, classNames);
    }

    /** Depth-parameterized variant — used by depth-3+ generators. */
    public static String emitInitialAggregator(int depth,
                                               String packageName,
                                               String aggregatorClassName,
                                               String subPackage,
                                               List<String> classNames) {
        List<String> sorted = new ArrayList<>(classNames);
        Collections.sort(sorted);

        StringBuilder sb = new StringBuilder();
        sb.append(bannerFor(depth)).append('\n');
        sb.append("package ").append(packageName).append(";\n\n");
        sb.append("import kranji.zi.ComposedZi;\n\n");
        for (String cn : sorted) {
            sb.append("import ").append(packageName).append('.').append(subPackage)
                    .append('.').append(cn).append(";\n");
        }
        sb.append('\n');
        sb.append("import java.util.ArrayList;\n");
        sb.append("import java.util.Collections;\n");
        sb.append("import java.util.List;\n\n");
        sb.append("public final class ").append(aggregatorClassName).append(" {\n\n");
        sb.append("    private ").append(aggregatorClassName).append("() {}\n\n");
        sb.append("    public static final List<ComposedZi> ALL;\n");
        sb.append("    static {\n");
        sb.append("        var list = new ArrayList<ComposedZi>();\n");
        for (String cn : sorted) {
            sb.append("        list.addAll(").append(cn).append(".ALL);\n");
        }
        sb.append("        ALL = Collections.unmodifiableList(list);\n");
        sb.append("    }\n");
        sb.append("}\n");
        return sb.toString();
    }

    // ── Top-level Depth2 aggregator ───────────────────────────────────

    /** Emit the rewritten {@code Depth2.java}. */
    public static String emitTopLevelAggregator(String packageName,
                                                List<String> initialAggregatorClassNames) {
        return emitTopLevelAggregator(2, packageName, initialAggregatorClassNames);
    }

    /** Depth-parameterized variant — emits {@code Depth<N>.java}. */
    public static String emitTopLevelAggregator(int depth,
                                                String packageName,
                                                List<String> initialAggregatorClassNames) {
        List<String> sorted = new ArrayList<>(initialAggregatorClassNames);
        Collections.sort(sorted);

        String topClass = "Depth" + depth;

        StringBuilder sb = new StringBuilder();
        sb.append(bannerFor(depth)).append('\n');
        sb.append("package ").append(packageName).append(";\n\n");
        sb.append("import kranji.zi.ComposedZi;\n\n");
        sb.append("import java.util.ArrayList;\n");
        sb.append("import java.util.Collections;\n");
        sb.append("import java.util.List;\n\n");
        sb.append("/**\n");
        sb.append(" * Depth-").append(depth).append(" composed characters: at least one direct component is itself a\n");
        sb.append(" * depth-").append(depth - 1).append(" composition.\n");
        sb.append(" *\n");
        sb.append(" * <p>Records are partitioned by pinyin (initial, final, tone) — see the\n");
        sb.append(" * {@code <initial>/<Final><Tone>.java} sub-package files. This class\n");
        sb.append(" * concatenates every per-initial aggregator into a single immutable list.</p>\n");
        sb.append(" */\n");
        sb.append("public final class ").append(topClass).append(" {\n\n");
        sb.append("    private ").append(topClass).append("() {}\n\n");
        sb.append("    /** All depth-").append(depth).append(" composed characters. */\n");
        sb.append("    public static final List<ComposedZi> ALL;\n\n");
        sb.append("    static {\n");
        sb.append("        var list = new ArrayList<ComposedZi>();\n");
        for (String cn : sorted) {
            sb.append("        list.addAll(").append(cn).append(".ALL);\n");
        }
        sb.append("        ALL = Collections.unmodifiableList(list);\n");
        sb.append("    }\n");
        sb.append("}\n");
        return sb.toString();
    }

    // ── Field emission ────────────────────────────────────────────────

    private static String emitField(Member m,
                                    SingularZiIndex singulars,
                                    ComposedZiIndex composed,
                                    ImportCollector imports) {
        ComposedZiJson z = m.data();
        StringBuilder sb = new StringBuilder();
        sb.append("    public static final ComposedZi ").append(m.fieldName())
                .append(" = new ComposedZi(\n");
        sb.append("            new ZiChar.Unicode(").append(quoteUnicode(z.glyph())).append("),\n");
        sb.append("            ").append(emitPinyin(z.pinyin())).append(",\n");
        sb.append("            ").append(z.strokes() == null ? 0 : z.strokes()).append(", ");
        sb.append(z.radicalNo() == null ? 0 : z.radicalNo()).append(", ");
        sb.append(quoteJava(z.meaning() == null ? "" : z.meaning())).append(",\n");
        sb.append("            ").append(emitBlock(z.composition(), singulars, composed, imports)).append(",\n");
        sb.append("            ").append(emitEtymology(z.etymology(), singulars, composed, imports));
        sb.append(");\n");
        return sb.toString();
    }

    // ── Composition ───────────────────────────────────────────────────

    private static String emitBlock(ComposedBlockJson block,
                                    SingularZiIndex singulars,
                                    ComposedZiIndex composed,
                                    ImportCollector imports) {
        String style = block.style();
        Map<String, BlockRefJson> slots = block.slots();
        return switch (style) {
            case "left_right"        -> "new LeftRight("
                    + emitRef(slots.get("left"),  singulars, composed, imports) + ", "
                    + emitRef(slots.get("right"), singulars, composed, imports) + ")";
            case "top_bottom"        -> "new TopBottom("
                    + emitRef(slots.get("top"),    singulars, composed, imports) + ", "
                    + emitRef(slots.get("bottom"), singulars, composed, imports) + ")";
            case "left_middle_right" -> "new LeftMiddleRight("
                    + emitRef(slots.get("left"),   singulars, composed, imports) + ", "
                    + emitRef(slots.get("middle"), singulars, composed, imports) + ", "
                    + emitRef(slots.get("right"),  singulars, composed, imports) + ")";
            case "top_middle_bottom" -> "new TopMiddleBottom("
                    + emitRef(slots.get("top"),    singulars, composed, imports) + ", "
                    + emitRef(slots.get("middle"), singulars, composed, imports) + ", "
                    + emitRef(slots.get("bottom"), singulars, composed, imports) + ")";
            case "full_enclosure"    -> "new FullEnclosure("
                    + emitRef(slots.get("outer"), singulars, composed, imports) + ", "
                    + emitRef(slots.get("inner"), singulars, composed, imports) + ")";
            case "semi_enclosure_upper_left"   -> emitSemi("SemiEnclosureUpperLeft",  slots, singulars, composed, imports);
            case "semi_enclosure_upper_right"  -> emitSemi("SemiEnclosureUpperRight", slots, singulars, composed, imports);
            case "semi_enclosure_bottom_left"  -> emitSemi("SemiEnclosureBottomLeft", slots, singulars, composed, imports);
            case "semi_enclosure_top_three"    -> emitSemi("SemiEnclosureTopThree",   slots, singulars, composed, imports);
            case "semi_enclosure_bottom_three" -> emitSemi("SemiEnclosureBottomThree",slots, singulars, composed, imports);
            case "semi_enclosure_left_three"   -> emitSemi("SemiEnclosureLeftThree",  slots, singulars, composed, imports);
            default -> throw new IllegalArgumentException("Unknown composition style: " + style);
        };
    }

    private static String emitSemi(String variantName,
                                   Map<String, BlockRefJson> slots,
                                   SingularZiIndex singulars,
                                   ComposedZiIndex composed,
                                   ImportCollector imports) {
        return "new " + variantName + "("
                + emitRef(slots.get("wrapper"), singulars, composed, imports) + ", "
                + emitRef(slots.get("content"), singulars, composed, imports) + ")";
    }

    private static String emitRef(BlockRefJson ref,
                                  SingularZiIndex singulars,
                                  ComposedZiIndex composed,
                                  ImportCollector imports) {
        if (ref == null) {
            throw new IllegalArgumentException("Missing slot in composition");
        }
        return switch (ref) {
            case BlockRefJson.GlyphRef g -> {
                // Priority 1: depth-1 ComposedZi (structure-valued). Emitted
                // as a fully-qualified expression — the simple class names
                // (e.g. An3, I4) collide between depth buckets, so importing
                // would shadow the current compilation unit's class.
                ComposedZiIndex.Ref cr = composed.find(g.glyph());
                if (cr != null) {
                    yield cr.structureExpression();
                }
                // Priority 2: singular glyph.
                SingularZiIndex.Ref sr = singulars.require(g.glyph());
                imports.addSingular(sr);
                yield sr.expression();
            }
            case BlockRefJson.InlineComposed ic -> "new ComposedPart(" + emitBlock(ic.block(), singulars, composed, imports) + ")";
            case BlockRefJson.InlineSingular is -> emitInlineSingular(is.part());
        };
    }

    private static String emitInlineSingular(SingularPartJson p) {
        throw new UnsupportedOperationException(
                "InlineSingular emission for glyph \"" + p.glyph()
                        + "\" is not implemented — depth2 catalog should "
                        + "always reference singulars by glyph (GlyphRef).");
    }

    // ── Etymology ─────────────────────────────────────────────────────

    private static String emitEtymology(EtymologyJson e,
                                        SingularZiIndex singulars,
                                        ComposedZiIndex composed,
                                        ImportCollector imports) {
        if (e == null) return "new Pictograph()";
        return switch (e) {
            case EtymologyJson.Pictograph p -> "new Pictograph()";
            case EtymologyJson.SimpleIndicative s ->
                    "new SimpleIndicative(" + quoteJava(s.indicatorDescription()) + ")";
            case EtymologyJson.CompoundIndicative c ->
                    "new CompoundIndicative(" + quoteJava(c.meaningHint()) + ")";
            case EtymologyJson.DerivativeCognate d ->
                    "new DerivativeCognate(" + quoteJava(d.cognateGlyph()) + ")";
            case EtymologyJson.PhoneticLoan l ->
                    "new PhoneticLoan(" + quoteJava(l.originalMeaning()) + ")";
            case EtymologyJson.PhonoSemantic ps -> "new PhonoSemantic("
                    + emitRef(ps.semanticPart(), singulars, composed, imports) + ", "
                    + emitRef(ps.phoneticPart(), singulars, composed, imports) + ")";
        };
    }

    // ── Pinyin ────────────────────────────────────────────────────────

    private static final Set<String> SYLLABIC_INITIALS_PINYIN = Set.of(
            "zh", "ch", "sh", "r", "z", "c", "s");

    private static String emitPinyin(PinyinJson py) {
        if (py == null) return "null";
        String initial = py.initial() == null ? "" : py.initial();
        String fin     = py.fin()     == null ? "" : py.fin();
        int    tone    = py.tone()    == null ? 5  : py.tone();
        if (fin.isEmpty() && SYLLABIC_INITIALS_PINYIN.contains(initial)) {
            fin = "i";
        }
        PinyinSyllable typed = PinyinSyllable.parse(initial + fin + tone);
        if (typed == null) return "null";
        return "new PinyinSyllable("
                + "Initial." + typed.initial().name()
                + ", new Final("
                + "Head." + typed.fin().head().name()
                + ", Body." + typed.fin().body().name()
                + ", Tail." + typed.fin().tail().name() + "), "
                + "Tone." + typed.tone().name() + ")";
    }

    // ── String quoting ────────────────────────────────────────────────

    private static String quoteUnicode(String s) {
        StringBuilder b = new StringBuilder("\"");
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c == '"')       b.append("\\\"");
            else if (c == '\\') b.append("\\\\");
            else if (c == '\n') b.append("\\n");
            else if (c == '\r') b.append("\\r");
            else if (c == '\t') b.append("\\t");
            else if (c >= 0x20 && c < 0x7F) b.append(c);
            else b.append(String.format("\\u%04X", (int) c));
        }
        b.append('"');
        return b.toString();
    }

    private static String quoteJava(String s) {
        StringBuilder b = new StringBuilder("\"");
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c == '"')       b.append("\\\"");
            else if (c == '\\') b.append("\\\\");
            else if (c == '\n') b.append("\\n");
            else if (c == '\r') b.append("\\r");
            else if (c == '\t') b.append("\\t");
            else                b.append(c);
        }
        b.append('"');
        return b.toString();
    }

    // ── Import collector ──────────────────────────────────────────────

    /**
     * Collects import paths and emits them in deterministic alphabetical
     * order. Non-{@code java.*} imports come first per Kranji convention.
     */
    private static final class ImportCollector {
        private final Set<String> core      = new TreeSet<>();
        private final Set<String> singulars = new TreeSet<>();

        void addCore(String fqn) { core.add(fqn); }
        void addSingular(SingularZiIndex.Ref r) { singulars.add(r.importFqn()); }

        List<String> sorted() {
            Set<String> nonJava = new TreeSet<>();
            Set<String> java    = new TreeSet<>();
            for (String s : core)      (s.startsWith("java.") ? java : nonJava).add(s);
            for (String s : singulars) (s.startsWith("java.") ? java : nonJava).add(s);
            List<String> out = new ArrayList<>();
            out.addAll(nonJava);
            out.addAll(java);
            return out;
        }

        /** Visible for tests. */
        Map<String, Set<String>> snapshot() {
            Map<String, Set<String>> snap = new TreeMap<>();
            snap.put("core", new TreeSet<>(core));
            snap.put("singulars", new TreeSet<>(singulars));
            return snap;
        }
    }
}
