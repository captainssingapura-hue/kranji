package kranji.codegen.perclass;

import kranji.json.dto.BlockRefJson;
import kranji.json.dto.ComposedBlockJson;
import kranji.json.dto.ComposedZiJson;
import kranji.json.dto.EtymologyJson;
import kranji.json.dto.PinyinJson;
import kranji.pinyin.PinyinSyllable;

import java.util.ArrayList;
import java.util.List;
import java.util.Map;
import java.util.Set;
import java.util.TreeSet;

/**
 * Stateless source-text builder for the per-class typed record files
 * (e.g. {@code n/i3/NiYou.java}).
 *
 * <p>Each record:
 * <ul>
 *   <li>Implements {@code ComposedZiT} plus its typed-layout variant
 *       ({@code LeftRightT<L,R>}, {@code SemiEnclosureUpperLeftT<W,C>}, …)
 *       with fully-resolved type parameters — no {@code BlockStructure}
 *       wildcards.</li>
 *   <li>Exposes a singleton {@code public static final X INSTANCE}.</li>
 *   <li>Overrides every slot accessor to return the concrete
 *       singular/composed reference.</li>
 *   <li>Overrides {@code glyph()}, {@code pinyin()}, {@code strokes()},
 *       {@code radicalNo()}, {@code meaning()}, {@code etymology()} and
 *       {@code layout()}.</li>
 * </ul>
 *
 * <p>Pure function of (routing key, JSON payload, singular index,
 * composed index). Identical input produces byte-identical output.</p>
 */
public final class PerclassJavaEmitter {

    public static final String AUTO_BANNER =
            "// AUTO-GENERATED by PerclassGenerateMain - do not edit by hand.";

    private PerclassJavaEmitter() {}

    /**
     * Emit the full source of one per-class typed record file.
     *
     * @param key      routing key (package, class name, field identifier)
     * @param z        parsed JSON payload
     * @param singulars singular-glyph reverse-index
     * @param composed composed-Zi reverse-index (self-referential — the
     *                 record being emitted is also in this map, but is
     *                 not expected to appear in its own composition)
     */
    public static String emitRecord(PerclassRouting.Key key,
                                    ComposedZiJson z,
                                    PerclassSingularIndex singulars,
                                    PerclassComposedIndex composed) {
        String owner = key.className();
        ImportCollector imports = new ImportCollector();
        imports.add("kranji.classification.EtymologicalCategory");
        imports.add("kranji.pinyin.Body");
        imports.add("kranji.pinyin.Final");
        imports.add("kranji.pinyin.Head");
        imports.add("kranji.pinyin.Initial");
        imports.add("kranji.pinyin.PinyinSyllable");
        imports.add("kranji.pinyin.Tail");
        imports.add("kranji.pinyin.Tone");
        imports.add("kranji.zi.ComposedZiT");
        imports.add("kranji.zi.CompositionLayoutT");

        // Layout interface import + construction params
        LayoutShape shape = layoutShapeFor(z.composition().style());
        imports.add("kranji.zi." + shape.typedInterfaceName());

        // Type parameters — recursive deep-type expressions for each slot.
        List<SlotEmission> slotEmissions = new ArrayList<>();
        for (String slotName : shape.slotNames()) {
            BlockRefJson ref = z.composition().slots().get(slotName);
            if (ref == null) {
                throw new IllegalArgumentException(
                        "Missing slot '" + slotName + "' in composition of "
                                + key.fqn());
            }
            SlotTypeCarrier tc = emitSlotType(ref, owner, singulars, composed, imports);
            // Also compute the value expression here (it needs imports too).
            String valueExpr = emitSlotValue(ref, owner, singulars, composed, imports);
            slotEmissions.add(new SlotEmission(
                    slotName, tc.typeExpression(), valueExpr));
        }

        // Etymology — computes imports and emits the expression.
        String etymologyExpr = emitEtymology(z.etymology(), owner, singulars, composed, imports);

        // ── Begin source ─────────────────────────────────────────────
        StringBuilder sb = new StringBuilder();
        sb.append(AUTO_BANNER).append('\n');
        sb.append("package ").append(key.packageName()).append(";\n\n");
        for (String imp : imports.sorted()) {
            sb.append("import ").append(imp).append(";\n");
        }
        sb.append('\n');

        // Type parameter list for the layout interface.
        StringBuilder typeParams = new StringBuilder();
        for (int i = 0; i < slotEmissions.size(); i++) {
            if (i > 0) typeParams.append(", ");
            typeParams.append(slotEmissions.get(i).typeExpr);
        }

        sb.append("public record ").append(key.className()).append("()")
          .append(" implements ComposedZiT, ")
          .append(shape.typedInterfaceName()).append('<').append(typeParams).append("> {\n\n");

        // INSTANCE
        sb.append("    public static final ").append(key.className())
          .append(" INSTANCE = new ").append(key.className()).append("();\n\n");

        // Slot accessors
        for (SlotEmission se : slotEmissions) {
            sb.append("    @Override public ").append(se.typeExpr).append(' ')
              .append(se.slotName).append("() { return ").append(se.valueExpr)
              .append("; }\n");
        }
        sb.append('\n');

        sb.append("    @Override public CompositionLayoutT layout() { return this; }\n\n");

        sb.append("    @Override public String glyph() { return ")
          .append(quoteUnicode(z.glyph())).append("; }\n\n");

        sb.append("    @Override public PinyinSyllable pinyin() {\n");
        sb.append("        return ").append(emitPinyin(z.pinyin())).append(";\n");
        sb.append("    }\n\n");

        sb.append("    @Override public int strokes() { return ")
          .append(z.strokes() == null ? 0 : z.strokes()).append("; }\n");
        sb.append("    @Override public int radicalNo() { return ")
          .append(z.radicalNo() == null ? 0 : z.radicalNo()).append("; }\n");
        sb.append("    @Override public String meaning() { return ")
          .append(quoteJava(z.meaning() == null ? "" : z.meaning())).append("; }\n\n");

        sb.append("    @Override public EtymologicalCategory etymology() {\n");
        sb.append("        return ").append(etymologyExpr).append(";\n");
        sb.append("    }\n");

        sb.append("}\n");
        return sb.toString();
    }

    // ── Layout shape table ────────────────────────────────────────────

    /**
     * Static description of one layout variant: its typed interface,
     * the anonymous {@code *PartT} record to use for inline composition,
     * and the ordered slot names (matching both {@link ComposedBlockJson}
     * keys and the typed interface's accessor methods).
     */
    record LayoutShape(String typedInterfaceName,
                       String partRecordName,
                       List<String> slotNames) {}

    static LayoutShape layoutShapeFor(String style) {
        return switch (style) {
            case "left_right"                 -> new LayoutShape("LeftRightT", "LeftRightPartT",
                    List.of("left", "right"));
            case "top_bottom"                 -> new LayoutShape("TopBottomT", "TopBottomPartT",
                    List.of("top", "bottom"));
            case "left_middle_right"          -> new LayoutShape("LeftMiddleRightT", "LeftMiddleRightPartT",
                    List.of("left", "middle", "right"));
            case "top_middle_bottom"          -> new LayoutShape("TopMiddleBottomT", "TopMiddleBottomPartT",
                    List.of("top", "middle", "bottom"));
            case "full_enclosure"             -> new LayoutShape("FullEnclosureT", "FullEnclosurePartT",
                    List.of("outer", "inner"));
            case "semi_enclosure_upper_left"  -> new LayoutShape("SemiEnclosureUpperLeftT", "SemiEnclosureUpperLeftPartT",
                    List.of("wrapper", "content"));
            case "semi_enclosure_upper_right" -> new LayoutShape("SemiEnclosureUpperRightT", "SemiEnclosureUpperRightPartT",
                    List.of("wrapper", "content"));
            case "semi_enclosure_bottom_left" -> new LayoutShape("SemiEnclosureBottomLeftT", "SemiEnclosureBottomLeftPartT",
                    List.of("wrapper", "content"));
            case "semi_enclosure_top_three"   -> new LayoutShape("SemiEnclosureTopThreeT", "SemiEnclosureTopThreePartT",
                    List.of("wrapper", "content"));
            case "semi_enclosure_bottom_three"-> new LayoutShape("SemiEnclosureBottomThreeT", "SemiEnclosureBottomThreePartT",
                    List.of("wrapper", "content"));
            case "semi_enclosure_left_three"  -> new LayoutShape("SemiEnclosureLeftThreeT", "SemiEnclosureLeftThreePartT",
                    List.of("wrapper", "content"));
            default -> throw new IllegalArgumentException("Unknown composition style: " + style);
        };
    }

    // ── Slot emission ────────────────────────────────────────────────

    /** Internal — slot accessor row. */
    private record SlotEmission(String slotName, String typeExpr, String valueExpr) {}

    /**
     * Recursive type expression for a slot.
     *
     * @return carrier holding the type-expression string plus (redundantly)
     *         a concrete-type hint — currently just the same string, kept
     *         as a record so future emitter variants can return more info.
     */
    static SlotTypeCarrier emitSlotType(BlockRefJson ref,
                                        String owner,
                                        PerclassSingularIndex singulars,
                                        PerclassComposedIndex composed,
                                        ImportCollector imports) {
        return switch (ref) {
            case BlockRefJson.GlyphRef g -> emitGlyphRefType(g.glyph(), owner, singulars, composed, imports);
            case BlockRefJson.InlineComposed ic -> emitInlinePartType(ic.block(), owner, singulars, composed, imports);
            case BlockRefJson.InlineSingular is ->
                    throw new UnsupportedOperationException(
                            "InlineSingular type emission not yet supported for glyph \""
                                    + is.part().glyph() + "\" — catalog should use GlyphRef.");
        };
    }

    record SlotTypeCarrier(String typeExpression, String concreteTypeForAccessor) {}

    private static SlotTypeCarrier emitGlyphRefType(String glyph,
                                                    String owner,
                                                    PerclassSingularIndex singulars,
                                                    PerclassComposedIndex composed,
                                                    ImportCollector imports) {
        PerclassSingularIndex.Ref sr = singulars.find(glyph);
        if (sr != null) {
            imports.add(sr.valueImport());
            // If the singular's nested simple name collides with the record's
            // own class name, we cannot import it with a single-type-import;
            // qualify via its type container instead (e.g. PeopleAndRoles.Zi).
            if (sr.nestedTypeSimple().equals(owner)) {
                imports.add(sr.typeContainerFqn());
                String qualified = sr.typeContainerSimple() + "." + sr.nestedTypeSimple();
                return new SlotTypeCarrier(qualified, qualified);
            }
            imports.add(sr.typeImport());
            return new SlotTypeCarrier(sr.typeExpression(), sr.typeExpression());
        }
        PerclassComposedIndex.Ref cr = composed.require(glyph);
        // For composed refs, the type is a top-level class in its own package;
        // if it collides with owner's simple name, fall back to FQN.
        if (cr.className().equals(owner)) {
            String fqn = cr.typeImport();  // the fully-qualified name
            return new SlotTypeCarrier(fqn, fqn);
        }
        imports.add(cr.typeImport());
        return new SlotTypeCarrier(cr.typeExpression(), cr.typeExpression());
    }

    private static SlotTypeCarrier emitInlinePartType(ComposedBlockJson block,
                                                      String owner,
                                                      PerclassSingularIndex singulars,
                                                      PerclassComposedIndex composed,
                                                      ImportCollector imports) {
        LayoutShape shape = layoutShapeFor(block.style());
        imports.add("kranji.zi." + shape.partRecordName());
        StringBuilder typeParams = new StringBuilder();
        for (int i = 0; i < shape.slotNames().size(); i++) {
            if (i > 0) typeParams.append(", ");
            BlockRefJson child = block.slots().get(shape.slotNames().get(i));
            SlotTypeCarrier childCarrier = emitSlotType(child, owner, singulars, composed, imports);
            typeParams.append(childCarrier.typeExpression());
        }
        String typeExpr = shape.partRecordName() + "<" + typeParams + ">";
        return new SlotTypeCarrier(typeExpr, typeExpr);
    }

    /**
     * Value expression for a slot.
     *
     * <p>For GlyphRef to a singular → {@code Outer.CONSTANT}. For GlyphRef
     * to a composed Zi → {@code ClassName.INSTANCE}. For InlineComposed →
     * {@code new <PartRecord><>(<recursive children>)}.</p>
     */
    static String emitSlotValue(BlockRefJson ref,
                                String owner,
                                PerclassSingularIndex singulars,
                                PerclassComposedIndex composed,
                                ImportCollector imports) {
        return switch (ref) {
            case BlockRefJson.GlyphRef g -> emitGlyphRefValue(g.glyph(), owner, singulars, composed, imports);
            case BlockRefJson.InlineComposed ic -> emitInlinePartValue(ic.block(), owner, singulars, composed, imports);
            case BlockRefJson.InlineSingular is ->
                    throw new UnsupportedOperationException(
                            "InlineSingular value emission not yet supported for glyph \""
                                    + is.part().glyph() + "\" — catalog should use GlyphRef.");
        };
    }

    private static String emitGlyphRefValue(String glyph,
                                            String owner,
                                            PerclassSingularIndex singulars,
                                            PerclassComposedIndex composed,
                                            ImportCollector imports) {
        PerclassSingularIndex.Ref sr = singulars.find(glyph);
        if (sr != null) {
            imports.add(sr.valueImport());
            return sr.valueExpression();
        }
        PerclassComposedIndex.Ref cr = composed.require(glyph);
        // For composed refs whose class name collides with owner, use FQN.
        if (cr.className().equals(owner)) {
            return cr.typeImport() + ".INSTANCE";
        }
        imports.add(cr.typeImport());
        return cr.valueExpression();
    }

    private static String emitInlinePartValue(ComposedBlockJson block,
                                              String owner,
                                              PerclassSingularIndex singulars,
                                              PerclassComposedIndex composed,
                                              ImportCollector imports) {
        LayoutShape shape = layoutShapeFor(block.style());
        imports.add("kranji.zi." + shape.partRecordName());
        StringBuilder args = new StringBuilder();
        for (int i = 0; i < shape.slotNames().size(); i++) {
            if (i > 0) args.append(", ");
            BlockRefJson child = block.slots().get(shape.slotNames().get(i));
            args.append(emitSlotValue(child, owner, singulars, composed, imports));
        }
        return "new " + shape.partRecordName() + "<>(" + args + ")";
    }

    // ── Etymology ────────────────────────────────────────────────────

    static String emitEtymology(EtymologyJson e,
                                String owner,
                                PerclassSingularIndex singulars,
                                PerclassComposedIndex composed,
                                ImportCollector imports) {
        if (e == null) return "new EtymologicalCategory.Pictograph()";
        return switch (e) {
            case EtymologyJson.Pictograph p ->
                    "new EtymologicalCategory.Pictograph()";
            case EtymologyJson.SimpleIndicative s ->
                    "new EtymologicalCategory.SimpleIndicative("
                            + quoteJava(s.indicatorDescription()) + ")";
            case EtymologyJson.CompoundIndicative c ->
                    "new EtymologicalCategory.CompoundIndicative("
                            + quoteJava(c.meaningHint()) + ")";
            case EtymologyJson.DerivativeCognate d ->
                    "new EtymologicalCategory.DerivativeCognate("
                            + quoteJava(d.cognateGlyph()) + ")";
            case EtymologyJson.PhoneticLoan l ->
                    "new EtymologicalCategory.PhoneticLoan("
                            + quoteJava(l.originalMeaning()) + ")";
            case EtymologyJson.PhonoSemantic ps ->
                    "new EtymologicalCategory.PhonoSemantic("
                            + emitSlotValue(ps.semanticPart(), owner, singulars, composed, imports) + ", "
                            + emitSlotValue(ps.phoneticPart(), owner, singulars, composed, imports) + ")";
        };
    }

    // ── Pinyin ───────────────────────────────────────────────────────

    private static final Set<String> SYLLABIC_INITIALS_PINYIN = Set.of(
            "zh", "ch", "sh", "r", "z", "c", "s");

    static String emitPinyin(PinyinJson py) {
        if (py == null) return "null";
        String initial = py.initial() == null ? "" : py.initial();
        String fin     = py.fin()     == null ? "" : py.fin();
        int    tone    = py.tone()    == null ? 5  : py.tone();
        if (fin.isEmpty() && SYLLABIC_INITIALS_PINYIN.contains(initial)) {
            fin = "i";
        }
        PinyinSyllable typed = PinyinSyllable.parse(initial + fin + tone);
        if (typed == null) {
            throw new IllegalArgumentException("Un-parsable pinyin: " + py);
        }
        return "new PinyinSyllable("
                + "Initial." + typed.initial().name()
                + ", new Final("
                + "Head." + typed.fin().head().name()
                + ", Body." + typed.fin().body().name()
                + ", Tail." + typed.fin().tail().name() + "), "
                + "Tone." + typed.tone().name() + ")";
    }

    // ── Quoting ──────────────────────────────────────────────────────

    static String quoteUnicode(String s) {
        StringBuilder b = new StringBuilder("\"");
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c == '"')       b.append("\\\"");
            else if (c == '\\') b.append("\\\\");
            else if (c == '\n') b.append("\\n");
            else if (c == '\r') b.append("\\r");
            else if (c == '\t') b.append("\\t");
            else if (c >= 0x20 && c < 0x7F) b.append(c);
            else b.append(String.format("\\u%04X", (int) c));
        }
        b.append('"');
        return b.toString();
    }

    static String quoteJava(String s) {
        StringBuilder b = new StringBuilder("\"");
        for (int i = 0; i < s.length(); i++) {
            char c = s.charAt(i);
            if (c == '"')       b.append("\\\"");
            else if (c == '\\') b.append("\\\\");
            else if (c == '\n') b.append("\\n");
            else if (c == '\r') b.append("\\r");
            else if (c == '\t') b.append("\\t");
            else                b.append(c);
        }
        b.append('"');
        return b.toString();
    }

    // ── Import collector ─────────────────────────────────────────────

    /**
     * Accumulates import FQNs and emits them in the project-standard
     * order — non-java first, java.* last, each group alphabetical.
     */
    static final class ImportCollector {
        private final Set<String> nonJava = new TreeSet<>();
        private final Set<String> java    = new TreeSet<>();

        void add(String fqn) {
            (fqn.startsWith("java.") ? java : nonJava).add(fqn);
        }

        List<String> sorted() {
            List<String> out = new ArrayList<>(nonJava.size() + java.size());
            out.addAll(nonJava);
            out.addAll(java);
            return out;
        }
    }

    // ── Registry emission ────────────────────────────────────────────

    /**
     * Emit the source for {@code AllPerclassRecords.java} — a single
     * immutable list of every {@code ComposedZiT} instance.
     *
     * @param packageFqn target package (e.g. {@code "kranji.common.perclass"})
     * @param entries    per-class record FQN + class name, in desired order
     */
    public static String emitRegistry(String packageFqn,
                                      List<PerclassRouting.Key> entries) {
        List<PerclassRouting.Key> sorted = new ArrayList<>(entries);
        sorted.sort((a, b) -> a.fqn().compareTo(b.fqn()));

        StringBuilder sb = new StringBuilder();
        sb.append(AUTO_BANNER).append('\n');
        sb.append("package ").append(packageFqn).append(";\n\n");

        // Only two imports — the record classes are referenced by FQN
        // because the per-pinyin sub-package class-names collide across
        // buckets (e.g. k.ong1.Kong vs k.ong3.Kong).
        sb.append("import kranji.zi.ComposedZiT;\n\n");
        sb.append("import java.util.List;\n\n");

        sb.append("/**\n");
        sb.append(" * Flat registry of every typed per-class record generated\n");
        sb.append(" * from the JSON catalog (depth1..5). Order is alphabetical by FQN.\n");
        sb.append(" *\n");
        sb.append(" * <p>Entries are referenced by fully-qualified name because some\n");
        sb.append(" * per-(initial, final+tone) simple class names (e.g. {@code Kong},\n");
        sb.append(" * {@code Kun}) appear in multiple buckets.</p>\n");
        sb.append(" */\n");
        sb.append("public final class AllPerclassRecords {\n\n");
        sb.append("    private AllPerclassRecords() {}\n\n");
        sb.append("    /** Every typed per-class record, immutable, alphabetical by FQN. */\n");
        sb.append("    public static final List<ComposedZiT> ALL = List.of(\n");
        for (int i = 0; i < sorted.size(); i++) {
            sb.append("            ").append(sorted.get(i).fqn()).append(".INSTANCE");
            sb.append(i == sorted.size() - 1 ? ");\n" : ",\n");
        }
        sb.append("}\n");
        return sb.toString();
    }

}
