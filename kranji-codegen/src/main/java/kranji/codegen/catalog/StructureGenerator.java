package kranji.codegen.catalog;

/**
 * Emits the .java source for one {@link CatalogRow} of a specific
 * composition structure.
 *
 * <p>Each implementation owns the template for one Layout code (e.g.
 * {@code "LR"}, {@code "TB"}, {@code "SemiUL"}). The catalog iterator
 * looks up the generator by {@link #layoutCode()} and skips rows whose
 * code has no registered generator (logged as "unsupported structure").</p>
 *
 * <p>Implementations must:</p>
 * <ul>
 *   <li>Produce <strong>byte-identical output</strong> for the same input
 *       (deterministic — alphabetised imports, no timestamps, no random
 *       ordering).</li>
 *   <li>Refuse silently if the resolved component count doesn't match
 *       {@link #arity()} — the iterator validates arity before calling
 *       {@link #emit(EmitContext)}.</li>
 *   <li>Emit the {@link #AUTO_BANNER} as the first line, so generated
 *       files are visually distinct from hand-authored ones.</li>
 * </ul>
 */
public interface StructureGenerator {

    String AUTO_BANNER =
            "// AUTO-GENERATED by CatalogIteratorMain - do not edit by hand.";

    /** The catalog Layout code this generator handles (e.g. {@code "LR"}). */
    String layoutCode();

    /** Number of components this layout takes (e.g. 2 for LR/TB, 3 for LR3/TB3). */
    int arity();

    /** Emit the full .java file body. */
    String emit(EmitContext ctx);
}
